#include <iostream>
#include <cmath>
using namespace std;

int main()
{
	
	cout << "Pow - " <<  pow(2, 3) << endl; // степень
	cout << "Abs - " <<  abs(-23) << endl; // модуль 
	cout << "Sin - " <<  sin(10) << endl; // синус 
	cout << "Cos - " <<  cos(10) << endl; // косинус 
	cout << "Sqrt - " <<  sqrt( 16) << endl; // квадрат
	cout << "Ceil - " <<  ceil( 24.69) << endl; // округление до большего
	cout << "Floor - " <<  floor( 24.69) << endl; // округление до меньшего
	cout << "Exp - " <<  exp( 24.69) << endl; // эспонент
	cout << "Log - " <<  log( 4) << endl; // эспонент

	
	
	
	return 0;
}
